// Variable declaration
let variavel = 10; //Let are mutable, you can change the value
variavel = 20;

const pi = 3.14; //Const are immutable, you can't change the value

// How to use the operators
// Sum
let sum = 10 + 10;
console.log(sum);

// Subtraction
let subtraction = 10 - 10;
console.log(subtraction);

// Multiplication
let multiplication = 10 * 10;
console.log(multiplication);

// Division
let division = 10 / 10;
console.log(division);

// Module
let module = 10 % 10;
console.log(module);

// Increment
let increment = 10;
increment++;
console.log(increment);

// Decrement
let decrement = 10;
